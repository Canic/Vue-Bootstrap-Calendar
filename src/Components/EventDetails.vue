<template>
    <transition name="modal">
        <div v-show="show">
            <div class="modal" @click.self="clickBackdrop">
                <div class="modal-dialog" ref="dialog">
                    <div class="modal-content">
                      
                        <div class="modal-header">
                            <button type="button" class="close" aria-label="Close" @click="cancel">
                                <span aria-hidden="true">&times;</span>
                            </button>
                            <h4 class="modal-title">{{event.title}}</h4>
                        </div>

                        <div class="modal-body">
                            <p>{{event.description}}</p>
                        </div>

                        <div class="modal-footer">
                            <button type="button" class="btn btn-danger" @click="cancel">{{ $t('generic.cancel') }}</button>
                            <button type="button" class="btn btn-warning" @click="deleteEvent">{{ $t('generic.delete_event') }}</button>
                        </div>
                    </div>
                </div>
            </div>
            <div class="modal-backdrop in"></div>
        </div>
    </transition>
</template>

<script>
    import {EVENT_DELETED} from '../actions';

    export default {
        
       props: {
           show: {
               type: Boolean,
               default: false
           },
           event: {
               type: Object,
               required: true,
           }
       },
       data () {
           return {
           };
       },
       created () {
           if (this.show) {
               document.body.className += ' modal-open';
           }
       },
       beforeDestroy () {
           document.body.className = document.body.className.replace(/\s?modal-open/, '');
       },
       watch: {
           show (value) {
               if (value) {
                   document.body.className += ' modal-open';
               }
           }
       },
       methods: {
           deleteEvent () {
               let me = this;

               this.$root.$emit(EVENT_DELETED, this.event);
               this.$emit('update:show', false);
           },
           cancel () {
               this.$emit('update:show', false)
           },
           clickBackdrop () {
               this.cancel();
           }
       }
    };
</script>

<style scoped>
    .modal {
        display: block;
    }
    .modal-enter-active, .modal-leave-active {
        transition: opacity .2s
    }
    .modal-enter, .modal-leave-to {
        opacity: 0
    }
    .modal-backdrop {
        opacity: 0.3;
    }
</style>